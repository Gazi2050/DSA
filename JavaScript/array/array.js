
// ✅ Basic Operations => a1
// push(value) – Adds an element to the end
// pop() – Removes and returns the last element
// unshift(value) – Adds an element to the beginning
// shift() – Removes the first element

// ✅ Slicing & Splicing => a2
// slice(start, end) – Returns a subarray [Does NOT Mutate Original]
// splice(start, deleteCount, newItem) – Adds/removes elements [Mutates Original]

// ✅ Searching => a3
// indexOf(value) – Returns index of the first occurrence
// includes(value) – Checks if an element exists
// find(callback) – Returns the first matching element
// findIndex(callback) – Returns the index of the first matching element

// ✅ Iteration => a4
// map(callback) – Creates a new array by applying a function to each element
// filter(callback) – Creates a new array with elements that pass a condition
// reduce(callback, initialValue) – Reduces an array to a single value
// forEach(callback) – Iterates over each element

// ✅ Sorting & Reversing => a5
// sort(compareFunction) – Sorts an array [Mutates Original Array]
// reverse() – Reverses an array in place

// ✅ Other Useful Methods => a6
// concat(array2) – Merges two arrays
// join(separator) – Converts an array to a string
// fill(value, start, end) – Fills part of an array with a value
